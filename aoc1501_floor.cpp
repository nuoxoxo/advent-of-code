#include <iostream>

int     main()
{
   int  count, pos, i;
   char c;

   count = 0;
   pos = 0;
   i = 0;
   while (std::cin >> c)
   {
       if (count == -1 && pos == 0)
       {
           pos = i;
       }
       if (c == '(')    count++;
       if (c == ')')    count--;
       i++;
   }
   
   std::cout
       << "Star 1 : " << count << '\n' 
       << "Star 2 : " << pos << '\n';
}
