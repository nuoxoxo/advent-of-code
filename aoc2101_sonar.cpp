#include "iostream"
#include "vector"

using   namespace std;

int     main()
{
        vector<int>	D;
	int		i;
        int         	p1 = 0;
	int		p2 = 0;

        while (cin >> i)
		D.push_back(i);
        i = 0;
        while (++i < D.size())
        {
		if (i > 0 && D[i] > D[i - 1])
			++p1;
		if (i > 2 && D[i] + D[i - 1] + D[i - 2] >
			D[i - 1] + D[i - 2] + D[i - 3])
			++p2;
	}
	cout << "Star 1: " << p1 << endl;
	cout << "Star 2: " << p2 << endl;
}
